package business;

public class ValoracionesDTO {
	
	
	String propietario;
	
	String criticas_titulo;
	
	int puntuacion;
	
	
	
	
	
	public ValoracionesDTO() {
		
	}
	
	
	public String getPropietario() {
		return propietario;
		
	}
	
	
	public void setPropietario(String propietario) {
		this.propietario = propietario;
		
	}
	
	public String getCriticasTitulo() {
		return criticas_titulo;
	}
	
	public void setCriticasTitulo(String criticas_titulo) {
		this.criticas_titulo = criticas_titulo;
	}
	
	public int getPuntuacion() {
		return puntuacion;
	}
	
	public void setPuntuacion(int puntuacion) {
		this.puntuacion = puntuacion;
		
	}
	
	
	
	
	

}
